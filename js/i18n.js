// ─── Helper: set innerHTML vs textContent safely ─────────────
function setText(selector, key, t, useHTML = false) {
    const el = document.querySelector(selector);
    if (!el) return;
    if (useHTML) el.innerHTML = t[key];
    else el.textContent = t[key];
}

function setAttr(selector, attr, key, t) {
    const el = document.querySelector(selector);
    if (!el) return;
    el.setAttribute(attr, t[key]);
}

function setAll(selector, key, t, useHTML = false) {
    document.querySelectorAll(selector).forEach(el => {
        if (useHTML) el.innerHTML = t[key];
        else el.textContent = t[key];
    });
}

// ─── Apply translations to the DOM ───────────────────────────
function applyTranslations(lang) {
    const t = translations[lang];

    // Meta & document
    document.documentElement.lang = t.lang;
    document.title = t.pageTitle;
    setAttr('meta[name="description"]', 'content', 'metaDescription', t);
    setAttr('meta[property="og:title"]', 'content', 'ogTitle', t);
    setAttr('meta[property="og:description"]', 'content', 'ogDescription', t);

    // Navigation links
    const navLinks = document.querySelectorAll('.nav-link');
    const navKeys = ['navHome', 'navAbout', 'navSkills', 'navProjects', 'navContact'];
    navLinks.forEach((link, i) => {
        if (navKeys[i]) link.textContent = t[navKeys[i]];
    });

    // Curriculum download buttons (desktop + mobile)
    document.querySelectorAll('.btn-download-cv').forEach(btn => {
        btn.setAttribute('href', t.navCurriculumFile);
        btn.setAttribute('download', '');          // garante que download nunca suma
        // Rebuild inner content preserving the icon
        btn.innerHTML = `<i class="ri-file-download-line" aria-hidden="true"></i> ${t.navDownloadCta}`;
    });

    // Mobile toggle aria-label
    setAttr('#mobileToggle', 'aria-label', 'navMobileAriaOpen', t);

    // ── Hero ──
    setText('.hero-subtitle', 'heroSubtitle', t, true);
    setText('.hero-description', 'heroDescription', t);
    const heroCtas = document.querySelectorAll('.hero-cta .btn');
    if (heroCtas[0]) heroCtas[0].textContent = t.heroCta1;
    if (heroCtas[1]) heroCtas[1].textContent = t.heroCta2;

    // ── About ──
    setText('#about .section-label', 'aboutLabel', t);
    setText('#about .section-title', 'aboutTitle', t, true);
    const aboutPs = document.querySelectorAll('.about-text p');
    if (aboutPs[0]) aboutPs[0].textContent = t.aboutP1;
    if (aboutPs[1]) aboutPs[1].textContent = t.aboutP2;

    const statLabels = document.querySelectorAll('#about .stat-label');
    const statKeys = ['stat1Label', 'stat2Label', 'stat3Label', 'stat4Label'];
    statLabels.forEach((el, i) => { if (statKeys[i]) el.textContent = t[statKeys[i]]; });

    // ── Manifesto ──
    setText('#manifesto .section-label', 'manifestoLabel', t);
    setText('#manifesto .section-title', 'manifestoTitle', t, true);
    const mCards = document.querySelectorAll('.manifesto-card');
    if (mCards[0]) {
        mCards[0].querySelector('h3').textContent = t.manifesto1Title;
        mCards[0].querySelector('p').textContent = t.manifesto1Text;
    }
    if (mCards[1]) {
        mCards[1].querySelector('h3').textContent = t.manifesto2Title;
        mCards[1].querySelector('p').textContent = t.manifesto2Text;
    }
    if (mCards[2]) {
        mCards[2].querySelector('h3').textContent = t.manifesto3Title;
        mCards[2].querySelector('p').textContent = t.manifesto3Text;
    }

    // ── Timeline ──
    setText('#timeline .section-label', 'timelineLabel', t);
    setText('#timeline .section-title', 'timelineTitle', t, true);
    const tlCards = document.querySelectorAll('#timeline .stat-card');
    const tlData = [
        ['timeline1Tag', 'timeline1Title', 'timeline1Text'],
        ['timeline2Tag', 'timeline2Title', 'timeline2Text'],
        ['timeline3Tag', 'timeline3Title', 'timeline3Text'],
        ['timeline4Tag', 'timeline4Title', 'timeline4Text'],
    ];
    tlCards.forEach((card, i) => {
        if (!tlData[i]) return;
        const label = card.querySelector('.stat-label');
        const h3 = card.querySelector('h3');
        const p = card.querySelector('p');
        if (label) label.textContent = t[tlData[i][0]];
        if (h3) h3.textContent = t[tlData[i][1]];
        if (p) p.textContent = t[tlData[i][2]];
    });

    // ── Skills ──
    setText('#skills .section-label', 'skillsLabel', t);
    setText('#skills .section-title', 'skillsTitle', t, true);
    setText('#skills .section-description', 'skillsDescription', t);

    // ── Projects ──
    setText('#projects .section-label', 'projectsLabel', t);
    setText('#projects .section-title', 'projectsTitle', t, true);
    setText('#projects .section-description', 'projectsDescription', t);
    const projDesc = document.querySelectorAll('.project-description');
    const projKeys = ['project1Description', 'project2Description', 'project3Description'];
    projDesc.forEach((el, i) => { if (projKeys[i]) el.textContent = t[projKeys[i]]; });
    setText('.more-projects .text-more', 'moreProjects', t, true);
    // Re-attach arrow after text replacement
    const moreLink = document.querySelector('.more-projects .text-more');
    if (moreLink) moreLink.innerHTML = `${t.moreProjects} <i class="fa-solid fa-arrow-right-long"></i>`;

    // ── Contact ──
    setText('#contact .section-label', 'contactLabel', t);
    setText('#contact .section-title', 'contactTitle', t, true);
    setText('#contact .section-description', 'contactDescription', t);
    const formLabels = document.querySelectorAll('.form-label');
    if (formLabels[0]) formLabels[0].textContent = t.formName;
    if (formLabels[1]) formLabels[1].textContent = t.formEmail;
    if (formLabels[2]) formLabels[2].textContent = t.formMessage;
    setAttr('#name', 'placeholder', 'formNamePlaceholder', t);
    setAttr('#email', 'placeholder', 'formEmailPlaceholder', t);
    setAttr('#message', 'placeholder', 'formMessagePlaceholder', t);
    setText('.form-button', 'formSubmit', t);

    // ── Footer ──
    setText('.copyright', 'copyright', t);
    // ── Language Button ──
    const btn = document.getElementById('lang-toggle-btn');
    if (btn) {
        btn.setAttribute('aria-label', t.langBtnLabel);
        btn.querySelector('.lang-btn-label').textContent = lang.toUpperCase();
    }
}

// ─── Public API ───────────────────────────────────────────────
export const I18n = {
    currentLang: 'pt',

    init() {
        // Load saved preference or default to 'pt'
        const saved = localStorage.getItem('portfolio-lang') || 'pt';
        this.currentLang = saved;

        this.injectButton();
        applyTranslations(this.currentLang);
        console.log(`I18n initialized. Language: ${this.currentLang}`);
    },

    toggle() {
        this.currentLang = this.currentLang === 'pt' ? 'en' : 'pt';
        localStorage.setItem('portfolio-lang', this.currentLang);
        applyTranslations(this.currentLang);

        // Animate the button
        const btn = document.getElementById('lang-toggle-btn');
        if (btn) {
            btn.classList.add('lang-btn-animate');
            setTimeout(() => btn.classList.remove('lang-btn-animate'), 400);
        }
    },

    injectButton() {
        const btn = document.createElement('button');
        btn.id = 'lang-toggle-btn';
        btn.className = 'lang-toggle-btn interactive';
        btn.setAttribute('aria-label', 'Switch to English');
        // Simple: globe icon + current language code
        btn.innerHTML = `
            <i class="ri-global-line lang-btn-icon" aria-hidden="true"></i>
            <span class="lang-btn-label">PT</span>
        `;
        btn.addEventListener('click', () => this.toggle());

        const navRight = document.querySelector('.nav-right');
        if (navRight) {
            navRight.insertBefore(btn, navRight.firstChild);
        }
    }
};
